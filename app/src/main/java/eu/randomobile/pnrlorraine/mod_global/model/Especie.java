package eu.randomobile.pnrlorraine.mod_global.model;

public class Especie {
    private String nid;
    private String title;
    private String description;
    private String body;
    private String image;
    private int type;
    private String typeName;
    private int espacios[];

    public Especie(String nid, String title,String description, String body, String image, int type, String typeName, int espacios[]) {
        this.nid = nid;
        this.title = title;
        this.description = description;
        this.body = body;
        this.image = image;
        this.type = type;
        this.typeName = typeName;
        this.espacios = espacios;
    }

    public Especie(String nid, String title, String body, String image, int type, String typeName, int espacios[]) {
        this.nid = nid;
        this.title = title;
        this.body = body;
        this.image = image;
        this.type = type;
        this.typeName = typeName;
        this.espacios = espacios;
    }

    public int[] getEspacios() {
        return espacios;
    }

    public void setEspacios(int[] espacios) {
        this.espacios = espacios;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        if (description!=null) {
            return description;

        } else {
            return "";
        }
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getBody() {
        if (body!=null) {
            return body;

        } else {
            return "";
        }
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getNid() {
        return nid;
    }

    public void setNid(String nid) {
        this.nid = nid;
    }

    public boolean isEspacioIn(int idEspacio) {
        boolean result = false;

        for (int id : espacios) {
            if (idEspacio == id) {
                result = true;
            }
        }

        return result;
    }
}